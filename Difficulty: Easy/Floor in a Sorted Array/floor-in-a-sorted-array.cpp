class Solution {
  public:

    int findFloor(vector<int>& arr, int k) {

        // Your code here
        int l=0;
        int h= arr.size();
        while(l<=h){
            int m = (l+h)/2;
            if(arr[m]<=k) l=m+1;
            else h=m-1;
        }
        return l-1;
    }
};


